import HttpService from '../services/HttpService';
import { mapGetters } from 'vuex';

export default {
    components: { NavbarRecrutador },
    data() {
        return {
            vaga: {
                title: '',
                work_model: '',
                job_type: '',
                jobs_state: '',
                jobs_city: '',
                jobs_description: '',
                recruiter_id: '',
